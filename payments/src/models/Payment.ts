import mongoose, { Schema, model } from 'mongoose';

interface IPayment {
  orderId: string;
  stripeId: string;
}

interface PaymentDocument extends mongoose.Document {
  orderId: string;
  stripeId: string;
}

interface PaymentModel extends mongoose.Model<PaymentDocument> {
  build(attrs: IPayment): PaymentDocument;
}

const paymentSchema = new Schema(
  {
    orderId: {
      type: String,
      required: true,
    },
    stripeId: {
      type: String,
      required: true,
    },
  },
  {
    toJSON: {
      transform(doc, ret) {
        ret.id = ret._id;
        delete ret._id;
      },
    },
  }
);

paymentSchema.statics.build = (attrs: IPayment) => {
  return new Payment(attrs);
};

const Payment = model<PaymentDocument, PaymentModel>('Payment', paymentSchema);

export default Payment;
